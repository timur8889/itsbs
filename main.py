        f"üîÑ *–ó–∞—è–≤–æ–∫ –≤ —Ä–∞–±–æ—Ç–µ:* {len(in_progress_requests)}\n"
        f"‚úÖ *–í—ã–ø–æ–ª–Ω–µ–Ω–Ω—ã—Ö –∑–∞—è–≤–æ–∫:* {len(completed_requests)}\n\n"
        "–í—ã–±–µ—Ä–∏—Ç–µ —Ä–∞–∑–¥–µ–ª –¥–ª—è –ø—Ä–æ—Å–º–æ—Ç—Ä–∞:"
